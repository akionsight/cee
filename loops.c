#include<stdio.h>

int main(void)
{
    printf("FOR LOOP \n");
    for (int i = 0; i <= 10; i++ ) {
        printf("LOL\n");
    }

    printf("DO WHILE LOOP \n");

    int i = 0;
    do {
        printf("LOL\n");
        i++;
    } while (i <= 10);
    
    printf("While loop\n");

    while (1)
    {
        printf("LOL\n");
    };
    
    return 0;
}
